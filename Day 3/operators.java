// types of operators 

public class operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        
        //Arithmetic Operators

        System.out.println("A + B = " + (a+b));
        System.out.println("A - B = " + (a-b));
        System.out.println("A * B = " + a*b);
        System.out.println("A / B = " + a/b);
        System.out.println("A % B = " + a%b);

        //Increment operators

        int c = 10,d=c++;
        System.out.println(c+ " ," + d);

        //Other types : Relations , Assignment , Bitwise , Logical
    }
}
